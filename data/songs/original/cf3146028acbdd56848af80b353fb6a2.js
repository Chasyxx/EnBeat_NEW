//instruments
static = t * [0x1, 0x2, 0x3 ,0x4][(t >> 1) % 4] * t / 0x1ff, // NEVER I USED RANDOM HERE

mel_kick = [0xfff, 0xfff, 0xfff, 0x3ff, 0xfff, 0xfff, 0xfff, 0x7ff][(t >> 12) % 8],

snare = (static % 0x100 * (-t & 0xfff) / 0x4e20) * [0x0,0x1][(t>>12) % 2],

o = (0x1) / 0xa, //bytebeat don't  have support of decimal numbers (for example 0.5) in base 16

mel = t * ([
  0x1, 0x2, 0x3, 0x4,
  0x1, 0x2, 0x3, 0x4, //the first part
  0x1, 0x2, 0x3, 0x4,
  0x1, 0x2, 0x3, 0x4,
  0x1 + (o * 0x2), 0x2 + (o * 0x4), 0x3 + (o * 0x6), 0x4 + (o * 0x8), 0x1 + (o * 0x2), 0x2 + (o * 0x4), 0x3 + (o * 0x6), 0x4 + (o * 0x8),
0x1 + (o * 0x2), 0x2 + (o * 0x4), 0x3 + (o * 0x6), 0x4 + (o * 0x8), 0x1 + (o * 0x2), 0x2 + (o * 0x4), 0x3 + (o * 0x6), 0x4 + (o * 0x8) // WHAT THE HELL
][(t >> 0xa) % 0x20]),

mel = mel * [
  0x1, 0x1 + (o * 0x6), 0x2 + (o * 0x4), 0x3 + (o * 0x2)
][(t >> 0x10) % 0x4], // YAAAYY!!!!!

kick = ( 0x493e0 /(t & mel_kick)) & 0x40,
(mel & 0x40) % 0x100 * (- t & 0xfff) / 0x7d0  + snare + kick